/////////////////////// Switch Statement//////////////////////
/* ex-1*/
var day;
day = 'friday';

switch(day){
    case 'saturday':
        console.log('I Like Saturday');
        break;
    case 'monday':
            console.log('I do not Like Monday');
            break;
    case 'friday':
            console.log('I really Like friday');

    default:
        console.log('I do not know the day');
    
}

